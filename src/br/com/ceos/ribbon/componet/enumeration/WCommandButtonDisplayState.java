package br.com.ceos.ribbon.componet.enumeration;

/**
 * Como os botões serão exibidos.
 * 
 * @author Wesley
 * @since 06 de Dezembro de 2014
 */
public enum WCommandButtonDisplayState {
  
  /**
   * Botão exibido em formato grande
   */
  BIG,
  
  /**
   * Botão exibido em formato tile, com título e descrição
   */
  TILE,
  
  /**
   * Botão exibido em formato médio
   */
  MEDIUM,
  
  /**
   * Botão exibido em formato pequeno
   */
  SMALL,
  
  /**
   * Botão assume o tamanho do ícone
   */
  FIT_TO_ICON
}
