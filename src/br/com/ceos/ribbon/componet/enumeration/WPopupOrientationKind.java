package br.com.ceos.ribbon.componet.enumeration;

/**
 * Enumeração dos tipos de orientação para um painel popup.
 * 
 * @author Wesley
 * @since 05 de Dezembro de 2014
 */
public enum WPopupOrientationKind {
  
  /**
   * Indica que o painel de popup será exibido abaixo do botão
   */
  ABAIXO,
  
  /**
   * Indica que o painel de popup será exibido do lado direito do botão
   */
  DIREITA,
  
  /**
   * Indica que o painel de popup será exibido do lado esquerdo do botão
   */
  ESQUERDA;
}
